package movable;

/**
 *
 * @author Munda, Hilaga, Unabia
 */
public interface Movable {
    /**
     * moves a point upwards
     */
    void moveUp();
    /**
     * moves a point downwards
     */
    void moveDown();
    /**
     * moves a point to the left
     */
    void moveLeft();
    /**
     * moves a point to the right;
     */
    void moveRight();
}
